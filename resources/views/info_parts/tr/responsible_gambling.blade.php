	<div class="toggleshow fontsize-header" ><h2 class="terms-condition">Responsible gambling<i class="icon-svernut-dly-vsex-stranic"></i></h2></div>

	<div class="togglediv color-white">
        <p>While some people gamble within their means – for some gambling can become a problem as it can turn into an addiction.</p>
        <p>When a gambler loses the sense of fun that casual gambling is meant to generate it means that he has a problem. He may begin to look at gambling as a way to make money or worse still he keeps on gambling to try and win back money he had previously lost. He often believes that he has special luck or abilities or that his luck may be about to change. When you gamble not just for that rush and you find yourself chasing losses and betting more, the result is a vicious circle of increasing losses and increased betting to recover the losses.</p>
        <p>As a player you must be responsible about gambling by knowing whether to gamble and how much money or time to spend.</p>

        <div class="terms-underline pace-inactive"><span><b>Responsible Gambling Tips</b></span></div>
        <p>For many people gambling is exciting and entertaining. They make careful decisions about how to spend their time and money, where to go and how to have fun.</p>



        <div class="terms-underline pace-inactive"><span><b>Here is how you can control your gambling:</b></span></div>
        <ul>
            <li>Decide on a loss limit ahead of time and stick to it</li>
            <li>Low-risk gambling is a way of playing with very little chance of losing control. The key to low-risk gambling is to realize that "it's only a game."</li>
            <li>Never borrow money to gamble</li>
            <li>Set a time limit</li>
            <li>Take frequent breaks</li>
            <li>Balance gambling with other activities</li>
            <li>Don't gamble when highly stressed, depressed, intoxicated or troubled in some other way</li>
            <li>Only gamble with money set aside for entertainment, never with money for everyday expenses</li>
        </ul>


        <div class="terms-underline pace-inactive"><span><b>You can realize you have a problem. </b></span></div>  
        <ul>
            <li>If you are leaving your family alone for long periods of time, in order to gamble</li>
            <li>If you arguing frequently with your partner about spending too much time and money on gambling and find that you cannot stop or limit yourself.</li>
            <li>If you are gambling family money needed for rent/mortgage, schooling, medical purposes, groceries, clothes, or heat.</li>
            <li>If you feel you have to gamble to get to spend time with people you understand?</li>
            <li>When you are with your family, at work or at social events, you find that your are constantly thinking about your next bet</li>
            <li>If you look at gambling as a way of making money fast.</li>
        </ul>
        <p>If your answer to any of the above is “YES” then we suggest that you take action immediately BEFORE it becomes a worse problem. There are many organizations that may help you to control your problem. You may also control yourself by taking immediate action and put limits on what you can do with your account with us.</p>


